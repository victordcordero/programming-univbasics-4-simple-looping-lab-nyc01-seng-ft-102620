require 'pry'

def loop_message_five_times(array)
  counter = 0
  while count < 5 do
    puts array
    counter += 1
  end
  end

def loop_message_n_times(array,limit)
  counter = 0
  while count < limit do
    puts array
    counter += 1
end
end


def output_array(array)
  counter = 0
  while array[counter] do
    puts array
    counter += 1
end
end

def return_string_array(array)
  counter = 1
  while array[counter] do
    return array
    counter += 0
end
end
